      more, the picture Basil painted of Dorian has noticeable blemish on it symbolizing the evil Dorian committed at
      the sacrifice of his soul. Given the horribleness of existence that Dorian has experienced (dead parents, his
      lover killed herself due to his reaction) he is clearly relieved to find an outlet to take the burden of evil
