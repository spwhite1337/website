        things as sins, are borrowed. He becomes an echo of someone else's music, an actor of a part that has not
        been written for him. The aim of life is self-development. To realize one's nature perfectly - that is what
        each of us is here for. People are afraid of themselves nowadays. They have forgotten the highest of all
        duties, the duty that one owes to one's self. Of course they are charitable. They feed the hungry and clothe
        the beggar. But their own souls starve, and are naked. Courage has gone out of our race. Perhaps we never
