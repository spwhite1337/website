        <h2>The Picture of Dorian Gray, by Oscar Wilde</h2>
        <hr>
         <b>Preface:</b> Set in the late 19th century in Victorian England, The Picture of Dorian Gray is the only novel
      written by the quotable Oscar Wilde and illustrates his philosophy of 'aestheticism'. The movement sought to
      create art for art's sake as expressed in the preface to the book in Mr. Wilde's own words:
         <br><br>
         <em>
           "All art is at once surface and symbol. Those who go beneath the surface do so at their peril. Those who
           read the symbol do so at their peril. It is the spectator, and not life, that art really mirrors. Diversity
           of opinion about a work of art shows that the work is new, complex, and vital. When critics disagree the
           artist is in accord with himself. We can forgive a man for making a useful thing as long as he does not
           admire it. The only excuse for making a useless thing is that one admires it intensely. All art is quite
           useless."
         </em>
         <br><br>
