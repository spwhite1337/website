      believes? This contradiction is by design as Oscar Wilde did not want a clear cut intention to be
      interpreted from his work just like a clear cut moral message is not to be drawn from it either.
      <br><br>
      Going forward, I see the book as a work of cynicism or criticism. It is primarily a reaction against a
      culture rather than a canonical work to initiate a movement. It is worth noting that Oscar Wilde was gay,
      prosecuted for sodomy, and existed in a period of serious moral conflict between changing philosophical
      movements, geopolitical movements, religious movements, and literary/artistic movements. <em>The Picture of
      Dorian Gray</em> responds to this time period by saying, "I don't believe in any of your shit but I can still
      write a great moral novel".
      <br><br>
      <b>Summary:</b> The story centers around three main characters: Dorian Gray, Lord Henry, and Basil.
      Dorian Gray, the protagonist, has a tragic past consisting of dead parents but exists today as a proud member
      of the upper class with a devastatingly handsome face. Lord Henry is a friend of Dorian's, clearly in love with
      him romantically, and espouses an extremely hedonistic moral system - live your life for the individual and fuck
      the rest. Basically a more honest version of <a href="https://en.wikipedia.org/wiki/Ayn_Rand" target = "_blank">
      Ayn Rand</a>'s shitty philosophy. Basil is an artist, also super in love with Dorian, and the general tempering
      force or voice for 'normalcy' in the novel.
