      <b>Morality of the Transcendent:</b> Lord Henry's views are contrasted with the thesis of aestheticism and
      the beliefs of Basil, the painter. Rather than self interest, humans strive for something greater than themselves -
      they create art for the sake of art or they try to capture the beauty of the world for the sake of beauty.
