      Gray</em> only touches on it to contrast it with Lord Henry and Dorian's hedonism. As stated in the preface,
      it is inherently contradictory to write a novel or create a piece of art for the sake of 'art' (transcendent)
      as the novel or piece of art will undoubtedly serve other interests including the author's.
      <br><br>
      <b>Moral Lesson:</b> Like ancient fables, I see <em>The Picture of Dorian Gray</em> as an educational lesson
      in morality, or a way to live. Self-interest is a dead end that systematically erodes the agent's soul (or self) 
      rather than strengthening it. Strive for the transcendent or good for goodness's sake instead. The book won't tell 
      you how to do it, but it will eloquently and humorously illustrate what failure looks like and why you should
      avoid the temptation of self-interest. The effect of evil is similar to depictions of sin throughout
      Christian culture as a systematic erosion of the soul and evacuation of your 'being' (see Tolkien's
      <a href="https://screenrant.com/lotr-facts-orcs-movies-leave-out/">Orcs</a>). The end result is
      hell where there is nothing. No soul, no beauty, no goals, no worth as evidenced by Dorian stabbing his
      soul in the end of the novel.
