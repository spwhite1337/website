      <b>Morality of Self-Interest:</b> The question of, 'How ought I direct my life?' is investigated by negation.
      Namely, systems of belief centered around self-interest, rationality, and complete altruism are outlined as
      containing unforeseen consequences or inherent contradictions. Oscar Wilde adopts a technique I love in
      literature where the reader is able to 'try out' certain systems of belief through a character experiencing
      events that serve as a 'moral lesson' from author to reader. In this work, the attempts to 'rationally'
      operate morally is illustrated to be so subjective, contradictory, and counter-productive that it isn't a moral
      system at all.
      <br><br>
      Throughout the novel, Lord Henry represents a self-interested or hedonistic morality and consistently impresses
      it upon Dorian Gray:
