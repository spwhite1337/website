    Mond lifts the curtain of society and outlines the mechanisms only referenced implicitly prior in the novel. The
    mechanisms of consumption and labor, Soma and sex, conditioning and suppression serve to elevate stability and
    "rationality" of the World State. The god of the State survives from the sacrifice of citizen's agency and humanity.
    Importantly, Mond doesn't really outline a "point" to the society other than the avoidance of bad things and
    stability and order they have glorified. John categorically rejects Mond's philosophy on the grounds it contradicts
    human nature.
    <br><br>
    After the conversation John's countenance deteriorates rapidly. He attempts to exile himself to a lighthouse to
    adopt a monkish and ascetic lifestyle. To his chagrin, the citizens follow and spectate his strange behavior akin
    to watching a zoo exhibit. Ultimately, he self-flagellates and hangs himself.
    <hr>
    <b>Marx and Liberalism:</b> Huxley brilliantly examines philosophies of politics and society in parallel with
    more substantive philosophies of humanity and human nature. Bernard's surname is not coincidentally Marx after the
    famous Viennese as his ideas permeate the World State's culture. The society is designed scientifically and the
    labor of the lower classes is utilized by the upper classes in accordance with the order of design. Importantly,
    Marx saw this as intermediate stage towards the final Utopia in contrast to the World State's view of these
    oppressive labor relations as the end in itself.
    <br>
    <captioned-image
        :image="getImgUrl('marx.jpg')" alt="Marx"
