     <b>Satan the Conspirator:</b> Focusing on Satan's plans, the poem starts with Satan's realization that he can never
    beat God and his army head on. He must instead underhandedly corrupt God's creation and win a game with rules he
    sets himself. Adam and Eve didn't have a chance because they were ultimately a tool for Satan's game.
     <br><br>
     The accusation John Milton leverages at Satan is the same structure that all prophets leverage against their enemy,
    "You are evil and motivated by revenge. So you are just going to pursue your goal without any ethical restraints and
    must be stopped!". More commonly, this takes the form of worshipping idols or false gods - similar to 'playing by
    rules you're not supposed to play'. In Milton's vision, the evil behavior is a product of his evil nature rather
    than accidental intentions that spiral off into evilness.
