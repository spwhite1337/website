     <br><br>
     <b>Adam and Eve:</b> While the theological implications of Paradise Lost are far too much for me to swallow, the perspective he takes on Adam and Eve is worth a comment. They initially exist as effectively equals - sharing a blissful love in Paradise. Eve commits the first sin, but due to deception by Satan. Adam follows suit, but of his own volition.
     <br><br>
     This belief reflects the controversial portrayal of genders in Christianity. In essence, our differences are irrelevant before God but our roles in society manifest differently. Adam is created first implying his role is to be the 'face' or 'head' of the Garden as a leader in service of God. Eve is created from Adam implying she is in service to both Adam and God. Later on, this relationship is expanded to include Jesus in the hierarchy as well.
     <br><br>
     To say something like, "Men are above women" or "Adam is above Eve" is too reductionist. They are both nothing without each other but can combine in harmony to properly orient themselves in life. Alone, Eve fell victim to deception by Satan's exploitation of her vanity. Adam's presence could have (i) mitigated her vane concerns by expressing his love to her or (ii) fortified her integrity in the face of temptation. Similarly, with a weakened Eve, Adam loses site of his purpose and perspective. He eats of the tree to regain the solidarity he failed to maintain.
