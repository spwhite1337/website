     The explicit depiction of Satan as evil by nature (and powerful) is not exactly accepted by the Catholic Church. There, evil does not exist in itself as all existence is a product of God and therefore good. However, Milton either disagreed with this theology or simplified it for use as a narrative device. The conniving, conspiracizing, underhanded Satan is in direct contrast to an honorable, just, and honest enemies found in other epics. The absolute power of these traits is too much for man alone, but trivial for God and/or Jesus to smite down.
